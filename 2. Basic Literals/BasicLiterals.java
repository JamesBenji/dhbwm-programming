public class BasicLiterals {
    public static void main(String[] args) {
        
        // integer literal, includes underscores to increase readability.
        // Integers are zero, positive or negative natural numbers
        int numberOfApples = 10_000_000;
        System.out.println(numberOfApples);

        // Character literal, a single symbol denoted within single quotes.
        // examples include: 'A','a', '0' up to '9', '$'
        char letter = 'A';
        System.out.println(letter);

        // String literal, a sequence of characters encpsulated by double quotes
        // examples include: "Hi there", "Is this right?", "10 + 20 = 30"
        String text = "Hi there";
        System.out.println(text);

    }
}
